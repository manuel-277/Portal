if (!function_exists('t')) {
    /**
     * Traduz uma chave para o idioma atual
     */
    function t($key) {
        global $translations, $user_lang;
        return $translations[$user_lang][$key] ?? $translations['pt'][$key] ?? $key;
    }
}

if (!function_exists('isLoggedIn')) {
    /**
     * Verifica se o usuário está logado
     */
    function isLoggedIn() {
        return !empty($_SESSION['user_id']);
    }
}

if (!function_exists('requireLogin')) {
    /**
     * Redireciona se o usuário não estiver logado
     */
    function requireLogin() {
        if (!isLoggedIn()) {
            header('Location: login.php');
            exit();
        }
    }
}

if (!function_exists('esc')) {
    /**
     * Escapa strings para saída HTML segura
     */
    function esc($str) {
        return htmlspecialchars($str, ENT_QUOTES, 'UTF-8');
    }
}

if (!function_exists('url')) {
    /**
     * Gera URL absoluta
     */
    function url($path = '') {
        $protocol = isset($_SERVER['HTTPS']) && $_SERVER['HTTPS'] === 'on' ? 'https' : 'http';
        $host = $_SERVER['HTTP_HOST'] ?? 'localhost';
        return "$protocol://$host" . rtrim(dirname($_SERVER['SCRIPT_NAME']), '/') . '/' . ltrim($path, '/');
    }
}

if (!function_exists('verificarVoto')) {
    /**
     * Verifica se um usuário já votou em um destino
     */
    function verificarVoto($pontoId, $usuarioId, $pdo) {
        if (!$usuarioId) return false;
        $stmt = $pdo->prepare("SELECT 1 FROM votos WHERE usuario_id = ? AND ponto_turistico_id = ?");
        $stmt->execute([$usuarioId, $pontoId]);
        return $stmt->rowCount() > 0;
    }
}

if (!function_exists('getTotalCurtidas')) {
    /**
     * Obtém o total de curtidas (estrelas) de um destino
     */
    function getTotalCurtidas($pontoId, $pdo) {
        $stmt = $pdo->prepare("SELECT COUNT(*) FROM votos WHERE ponto_turistico_id = ?");
        $stmt->execute([$pontoId]);
        return (int) $stmt->fetchColumn();
    }
}
